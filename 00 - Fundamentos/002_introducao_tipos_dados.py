# Usando aspas simples
print('Este é uma string')

# Usando aspas duplas
print("Texto")

# Escape
print("Mateus \"Henrique\"")  # \" - pula o proximo caracter, nao precisa ler
print("Mateus \nHenrique")

# r
print(r"Mateus \"Henrique\"")  # expressão regulares

# mais usado

print(1, "Mateus 'Henrique'")
