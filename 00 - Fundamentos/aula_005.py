"""
conversão de tipos, coerção
type convertion, typecasting, coercion
É o ato de converter um tipo em outro tipos imutáveis e primitivos:
str, int, float, bool

"""

# print("1" + 1)
print(int("1") + 1)
print(float("2") + 2)
print(bool())  # Uma string vazia é considerada falsa
print(bool(" "))  # Uma string, mesmo com espaço vazio é considerado verdadeiro
print(str(11) + "b")
